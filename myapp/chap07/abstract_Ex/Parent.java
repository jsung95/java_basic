package abstract_Ex;

public class Parent {

	public String name;
	public int age;
	
	
	public Parent() {
		;;
	}//default constructor
	
	public Parent(String name, int age) {
		this.name = name;
		this.age = age;
	}//constructor
	
	public String getName() {
		return this.name;
	}//getName
	
	
}//end class
