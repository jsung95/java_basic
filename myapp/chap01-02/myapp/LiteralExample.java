package myapp;

public class LiteralExample {
	
	// 변수를 사용 ---> 반드시, 변수가 "정의"(= 선언 + 초기화 )

	public static void main(String[] args) {
		int var1 = 10; // 10 진수로 초기화 -> 변수의 정의
		System.out.println(var1);
		
		int var2 = 010; // 8진수로 초기화 -> 변수의 정의
		System.out.println(var2);
		
		int var3 = 0x10; // 16진수로 대입 -> 변수의 정의
		System.out.println(var3);
		
		
		
		// 한 학생의 학번, 이름, 성적을 저장할 변수를 "정의" 하세요
		// 고려사항 : 변수에 저장할 값의 성질을 생각하고
		// 			변수의 (1) 타입, (2) 이름, (3) 초기값을 잘 결정하세요
		String studentNumber = "20141234";
		String name = "홍길동";
		double score = 3.8;
		
		// 두 숫자를 더해서, 그 결과를 보관할 필요가 있다
		// 이 목적에 맞게 변수를 정의하세요
		
		
		
	} // main
	
} // end class
